<h1>Website Policy</h1>
    <div class="container">

        <h2>1. Membership Policy</h2>
        <p>1.1 Eligibility:</p>
        <ul>
            <li>Membership is open to individuals aged 18 years and above.</li>
            <li>Members must complete the registration process and pay the annual membership fee of RM100.</li>
        </ul>
        <p>1.2 Membership Renewal:</p>
        <ul>
            <li>Membership is valid for one year and must be renewed before the expiry date to maintain active status.</li>
            <li>Members with lapsed memberships are not eligible for benefits until renewal.</li>
        </ul>
        <p>1.3 Membership Benefits:</p>
        <ul>
            <li>Access to all club facilities.</li>
            <li>20% discount on merchandise.</li>
            <li>Free participation in tournaments organized by the club.</li>
        </ul>

        <h2>2. Code of Conduct</h2>
        <p>2.1 Respect and Behavior:</p>
        <ul>
            <li>Members and visitors are expected to treat all players, staff, and spectators with respect.</li>
            <li>Discriminatory, abusive, or violent behavior will not be tolerated and may result in termination of membership.</li>
        </ul>
        <p>2.2 Sportsmanship:</p>
        <ul>
            <li>Members must adhere to the principles of fair play and sportsmanship during all activities.</li>
            <li>Complaints about unsportsmanlike behavior must be reported to the club administration.</li>
        </ul>
        <p>2.3 Use of Social Media:</p>
        <ul>
            <li>Members should use social media responsibly and refrain from posting offensive or harmful content related to the club.</li>
        </ul>

        <h2>3. Safety and Equipment</h2>
        <p>3.1 Safety Guidelines:</p>
        <ul>
            <li>Members must adhere to all safety rules and instructions provided by the club.</li>
            <li>Proper sports attire, including footwear, must be worn during training and matches.</li>
        </ul>
        <p>3.2 Use of Equipment:</p>
        <ul>
            <li>Club equipment must be used responsibly.</li>
            <li>Damaged or lost equipment due to negligence may require compensation.</li>
        </ul>

        <h2>4. Tournament Participation</h2>
        <p>4.1 Eligibility:</p>
        <ul>
            <li>Only active members are eligible to participate in club-organized tournaments.</li>
            <li>Participants must register for tournaments within the specified timeframe.</li>
        </ul>
        <p>4.2 Conduct During Tournaments:</p>
        <ul>
            <li>Members are expected to follow all rules and regulations of the tournament.</li>
            <li>Decisions made by referees and tournament organizers are final and must be respected.</li>
        </ul>

        <h2>5. Fees and Payments</h2>
        <p>5.1 Membership Fee:</p>
        <ul>
            <li>The annual membership fee is RM100 and is non-refundable.</li>
        </ul>
        <p>5.2 Merchandise Purchases:</p>
        <ul>
            <li>Members can purchase club merchandise through the official club platform.</li>
            <li>Discounts will be automatically applied for active members.</li>
        </ul>

        <h2>6. Termination of Membership</h2>
        <p>6.1 Voluntary Termination:</p>
        <ul>
            <li>Members may terminate their membership at any time by submitting a written request to the club.</li>
        </ul>
        <p>6.2 Involuntary Termination:</p>
        <ul>
            <li>Membership may be terminated for violations of club rules, including failure to pay fees, unsportsmanlike behavior, or misconduct.</li>
        </ul>

        <h2>7. Privacy and Data Protection</h2>
        <p>7.1 Personal Data:</p>
        <ul>
            <li>The club collects personal data for administrative purposes only and ensures its confidentiality.</li>
            <li>Members have the right to access and update their personal information.</li>
        </ul>
        <p>7.2 Photographs and Media:</p>
        <ul>
            <li>By participating in club events, members consent to the use of their images for promotional purposes.</li>
        </ul>

        <h2>8. Amendments to Policies</h2>
        <p>
            The club reserves the right to amend this policy at any time. Members will be notified of significant changes through official communication channels.
        </p>

        <h2>9. Contact Us</h2>
        <p>
            For any questions or concerns regarding this policy, please contact us via email at <a href="mailto:contact@knicksnetball.com">contact@knicksnetball.com</a> or visit our office at Knicks Netball Club.
